<p>No início da tarde desta terça-feira, o Grêmio se reapresentou no Centro de Treinamento Presidente Luiz Carvalho, para o último treino antes da viagem a Maceió, no estado de Alagoas. O Tricolor enfrenta o CSA, na quinta-feira, às 21h30, no Estádio Rei Pelé, pela 14ª rodada do Campeonato Brasileiro Série B, com o objetivo de somar pontos e se consolidar no G-4 da competição. </p>

<p>Depois de retomar as atividades na tarde de ontem, o grupo voltou aos trabalhos direcionados ao adversário desta semana. Inicialmente, o preparador Reverson Pimentel orientou um treino físico, marcado por exercícios preventivos e de mobilidade, seguidos de trabalhos com bola, que aperfeiçoaram passes, ritmo, movimentação e finalização em mini goleiras. </p>

<p>Enquanto isso, em outra parte do campo, o preparador de goleiros Mauri Lima trabalhou com os arqueiros Gabriel Grando, Adriel e Felipe Sheibig, separadamente. </p>

<p>Na segunda parte da tarde, foi a vez do comandante gremista, Roger Machado, conduzir uma atividade com o grupo de jogadores, mas todos os trabalhos técnicos e táticos foram de portões fechados para a imprensa. Por isso, não foi possível acompanhar os últimos ajustes no time que deverá entrar em campo no duelo. </p>

<p>Os trabalhos que começaram no início da tarde, seguiram até pouco mais de 16h, quando o elenco iniciou então a preparação para a viajem com destino a Maceió, com voo previsto ainda nesta noite. </p>

<p>Na tarde desta quarta-feira, o plantel gremista realiza a última atividade antes do confronto, no CT Ninho do Galo, já na capital alagoana. </p>

