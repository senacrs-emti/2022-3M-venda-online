<p>Com o apoio da torcida tricolor na Arena, que apoiou durante os 90 minutos, o Grêmio conquistou os três pontos no Campeonato Brasileiro Série B, diante do Novorizontino. A partida, que foi disputada na noite desta terça-feira, 7, terminou com o placar de 2 a 0 para a equipe de Roger Machado. Os gols foram marcados por Diego Souza e Janderson. </p>


<p>Com o resultado, a equipe gremista encerra a 11ª rodada na quinta colocação do campeonato, com 17 pontos. </p>

<p>Dentro de campo, o técnico Roger Machado manteve a formação da equipe com três zagueiros, mas contou com desfalques. Na defesa, Bruno Alves com sintomas gripais e Brenno, que sentiu um desconforto na panturrilha durante o aquecimento, estiveram fora da partida. No ataque, Janderson entrou na vaga de Benitez, lesionado. </p>

<p>Nos primeiros minutos, o Grêmio encontrou um adversário retrancado, mas a marcação não mudou a postura do Tricolor, que trocava passes e priorizava a posse de bola, em busca o ataque. O primeiro lance de perigo da partida veio aos sete minutos, a favor da equipe de Roger Machado. Edilson cruzou na cabeça de Diego Souza, que finalizou, mas a bola acabou desviando na defesa, e passou perto da trave. </p>

<p>Com nove minutos jogados, foi a vez de Biel tentar abrir o marcador. O atacante finalizou de fora de área, mas a bola subiu demais. Quatro minutos depois, Bitello chutou da entrada da grande área, porém sem perigo para o goleiro adversário, que viu a bola sair pela linha de fundo. </p>

<p>Todas as chances da partida eram a favor do Grêmio, Gabriel Grando mal trabalhava. Aos 16’, Nicolas cobrou falta pelo lado esquerdo na cabeça de Rodrigues, e, na finalização, faltou pouco para a bola entrar. A pressão seguiu. Com 23’ jogados, a equipe de Roger Machado teve a melhor chance do jogo. Janderson avançou pela direita, cruzou e, na finalização, Bitello chutou forte, fazendo o goleiro Giovanni realizar uma grande defesa. No rebote, Biel quase empurrou a bola para o fundo das redes. </p> 

<p>Aos 32’, a insistência parecia ter dado resultado. O gol saiu, mas a arbitragem viu impedimento de Bitello, que havia concluído para gol.  </p>

<p>A primeira vez que Gabriel Grando precisou trabalhar, foi aos 38’. Ronaldo cabeceou na entrada da pequena área, nas mãos do goleiro gremista, sem grande perigo. </p>

<p>No último minuto da primeira etapa, lances polêmicos aconteceram. Primeiro, Diego Souza chutou e a finalização pegou no zagueiro Rodolfo, fazendo os gremistas pedirem pênalti. O árbitro mandou o jogo seguir. Logo depois, a bola bateu no mesmo jogador, após um chute de Geromel, e a arbitragem confirmou a penalidade no VAR. Diego Souza cobrou e abriu o placar na Arena. </p> 

<p>O Grêmio retornou para a segunda etapa sem modificações e, assim como na primeira, os minutos iniciais foram de muito toque de bola do Tricolor. O ponteiro passava dos 10’, e nenhuma finalização era contabilizada, mas na primeira oportunidade, aos 13’, o Grêmio ampliou o marcador. A jogada começou com Nicolas, que tocou para Biel e ela acabou sobrando para Janderson, que estufou as redes. </p>

<p>Roger Machado realizou a primeira alteração na equipe, aos 16’. Diego Souza saiu para entrada de Elkeson. Três minutos depois, Biel pifou Janderson que avançou pela direita e bateu cruzado, para a grande defesa do goleiro adversário. </p>

<p>Na frente do placar, o Grêmio seguiu pressionando e levando perigo à meta de Giovanni. Com 22’, Elkeson cabeceou, após cobrança de escanteio, mas a bola foi para fora. </p> 

<p>Aos 25’, mais trocas aconteceram no Tricolor. Lucas Silva e Elias entraram nas vagas de Thiago Santos e Biel. A partida deu uma esfriada nos minutos finais e fez Roger Machado modificar, pela última vez, a equipe. Sarará e Gabriel Silva foram os escolhidos para entrarem nos lugares de Bitello e Janderson. </p>

<p>O Novorizontino até tentou pressionar a defesa gremista no final do jogo, mas não levou dificuldades para Grando. </p>

<p>Final de jogo: Grêmio 2x0 Novorizontino </p>

<p>O próximo desafio do Grêmio será na próxima segunda-feira, 13, quando enfrenta o Sport, em Recife. </p>
