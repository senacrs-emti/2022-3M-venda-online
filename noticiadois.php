<div id="textoquatro">
<p>Na tarde desta terça-feira, 07, ocorreu a cerimônia de apresentação do projeto social “Futuro Suave do Gramado ao Tatame.” A solenidade, realizada no saguão da Tribuna de Honra do Conselho Deliberativo da Arena,  foi prestigiada pelo vice-presidente do CD do Grêmio, Alexandre Bugin,  pelo presidente do Conselho de Administração, Romildo Bolzan, pelo vice-presidente Guto Peixoto, pelo diretor do Departamento de Responsabilidade Social do Tricolor, Rodrigo Kandrik, além do Secretário Estadual Adjunto do Esporte e Lazer, Bruno Ortiz, e da Secretária Municipal de Esporte, Lazer e Juventude, Débora Garcia, representantes da Comunidade TRI, executivos do Grêmio e da Arena. </p>  

<p>O campeão mundial e faixa preta de Jiu Jitsu, Nato Werlang, abriu o evento saudando a todos os presentes e contando um pouco da história que deu início à iniciativa: “Consegui, com a força do esporte, conquistar muitos sonhos. Hoje estamos materializando mais um por meio da Lei de Incentivo ao Esporte (LIE), que nos oportuniza estender a estas crianças tudo aquilo que aprendemos com a prática desta arte marcial. Colhemos depoimentos muito importantes com as famílias que fazem parte do projeto sobre mudanças comportamentais na escola e em casa. Tudo isso só reforça a convicção de que seguimos no caminho certo.” </p> 

<p>A representante da Comunidade Tri, Claudia Bueno, agradeceu aos que estiveram “dispostos a ver o projeto sob um olhar diferente:” “Estas crianças irão se tornar referência para muitas outras, nos permitindo voar mais alto. Nosso muito obrigado ao Grêmio, a todos os parceiros aqui presentes e a esta comunidade tão carente e que está sendo premiada com o projeto Futuro Suave do Gramado ao Tatame. Eu vivi para viver tudo isso e sou muito grata por tanto”, destacou Claudia, em uma fala emocionada. </p> 

<p>O encerramento da cerimônia contou com a palavra do mandatário gremista Romildo Bolzan, que também se mostrou satisfeito em poder entregar à comunidade um projeto lincado ao esporte. “O Grêmio se sente muito honrado em participar de um projeto desta magnitude. Os alunos têm uma oportunidade fantástica de autodesenvolvimento e quem sabe até de despertar talentos promissores. Servir como ponte para realizações como as que foram postas aqui é, sem dúvida, motivo de muito orgulho para todos nós”, disse ele. </p>   

<p>Ao final, professores e alunos realizaram uma demonstração da prática esportiva, num tatame posicionado em frente aos convidados. </p> 

<p>Também estiveram presentes à solenidade, o Secretário Adjunto da Secretaria Municipal de Esporte, Lazer e Juventude, Nelson Beron,  a Subprefeita do Bairro Humaitá, Tenente Daniela Patussi, o procurador geral de Porto Alegre, Dr. Roberto Rocha, o presidente da Arena do Grêmio, Mauro Araújo, diretores presidentes da empresa Bondamann, Ernani Follmann e Willian Bond, além do Major Gelson, Secretário Municipal adjunto de Segurança e Vinícius Kaster do Conselho Regional de Educação Física. O evento foi acompanhado também  pelo campeão mundial de judô, João Derly, w por Robinson Viana,   representando a ABT Materiais Elétricos, patrocinadora do projeto. </p> 

<p>O Projeto </p> 

<p>Com o objetivo de atender crianças que vivem em situação de vulnerabilidade social a ter um futuro mais digno, tendo o esporte como princípio de vida, e ao mesmo tempo ajudar a evitar a evasão escolar, o Grêmio lançou o projeto “Futuro Suave do Gramado ao Tatame”. </p> 

<p>Criado no início deste ano, por meio de uma parceria com a Associação de Gerenciamento de Projetos Sociais (AGPS), o projeto visa reunir crianças entre 7 e 15 anos, às terças e quintas-feiras, em horários inverso ao da escola regular, para aulas de Jiu-Jitsu. As atividades, que terão como sede o hall da Tribuna de Honra do Conselho Deliberativo da Arena, seguirão até dezembro de 2022, e serão ministradas por sete professores especializados na arte. </p> 

